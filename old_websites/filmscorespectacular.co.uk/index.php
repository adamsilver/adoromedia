				<h1>Film Score Spectacular - The Colosseum, Watford</h1>
				<p>Friday 2nd and Saturday 3rd October 2009 saw the hugely ambitious and successful world premiere production 
				of Film Score Spectacular...LIVE! at The Watford Colosseum. The event, commemorating some of the most popular 
				film scores of all time such as Lord of the Rings, Gladiator, Jaws, Superman and Schindler's List, was attended 
				by almost 2,000 people who were able to experience first hand the Colosseum's international reputation for its 
				sound quality produced by the mighty 100 piece Hertfordshire Philharmonia. Expertly conducted by Bjorn Bantock, 
				they delivered over 100 minutes of thunderous and moving film music brilliantly accompanied by a fantastic light show.</p>
				
				<p>Among the highlights was the Westminster School Boy's Choir treble soloist, Ismail Salim, who performed a 
				beautiful rendition of the Lord of the Rings and was worthy of the standing ovation at the end of the first half. 
				The Haberdashers School rock trio of Ishwar Mahesh on drums, Ben Cherkas on bass and lead guitarist Omar Ali, 
				performed the inspirational rock licks on Gonna Fly Now from Rocky. In contrast the audience also enjoyed the 
				haunting violin solo in Schinlders' List by the leader of the Hertfordshire Philharmonia.</p>
				
				<p>Both evenings were compered by popular presenter and performer, Alasdair Malloy, who provided a compelling and 
				entertaining narrative into each score and explained how some of the original film scores were actually recorded in 
				The Colosseum. The finale included the special compilation of music scores from all six Star Wars films.</p>
				
				<p>A spokesman for the producers, Richtrend, commented 'the feedback that we had after the show was superb and 
				everyone appeared to thoroughly enjoy themselves. We hope to bring the event back to the Colosseum following its 
				refurbishment next year with an even more spectacular concert!'</p>
				
				
				
